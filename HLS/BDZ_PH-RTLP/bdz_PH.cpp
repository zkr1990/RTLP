void bdz_PH(data_t &key, data_t &r, pn_t &pn) {
// #pragma HLS ARRAY_RESHAPE variable=g cyclic factor=2 dim=1
// #pragma HLS ARRAY_RESHAPE variable=res cyclic factor=8 dim=1
// #pragma HLS ARRAY_MAP variable=g instance=BRAM1 horizontal
// #pragma HLS ARRAY_MAP variable=res instance=BRAM1 horizontal
// merge two arrays to reduce the cost of BRAM

#pragma HLS RESOURCE variable=res core=ROM_2P_BRAM
#pragma HLS RESOURCE variable=g core=ROM_2P_BRAM
#pragma HLS RESOURCE variable=lookup_table core=ROM_2P_BRAM

#pragma HLS INTERFACE ap_ovld port=pn
#pragma HLS INTERFACE ap_vld port=r
#pragma HLS INTERFACE ap_vld port=key

	ap_uint<16> vertex;
	data_t hl[3] = {24,18,12315};
	// fake hash logic here, the hash consists of a simple XOR operation
	//hash(key, hl);
	//hl[0] += k >> 1;
	hl[0] ^= k;
	//hl[1] += k >> 2;
	hl[1] ^= k;
	//hl[2] += k >> 3;
	hl[2] ^= k;
	// end of fake hash logic

	hl[0] = hl[0]%r;
	hl[1] = hl[1]%r + r;
	hl[2] = hl[2]%r + (r << 1);

	ap_uint<8> byte0 = g[hl[0]/5];
	ap_uint<8> byte1 = g[hl[1]/5];
	ap_uint<8> byte2 = g[hl[2]/5];

	byte0 = lookup_table[hl[0]%5][byte0];
	byte1 = lookup_table[hl[1]%5][byte1];
	byte2 = lookup_table[hl[2]%5][byte2];
	vertex = hl[(ap_uint<32>)(byte0 + byte1 + byte2)%3];

	pn = res[vertex];
	return ;
}
